int	ft_iterative_factorial(int nb)
{
	int i;
	int rt;

	i = 2;
	rt = 1;
	if (nb < 0 || nb > 12)
		return (0);
	while (nb >= i)
	{
		rt = rt * i;
		i++;
	}
	return (rt);
}
